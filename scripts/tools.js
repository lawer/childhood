document.addEventListener("alpine:init", () => {
  Alpine.data("toolsApp", () => ({
    lang: document.documentElement.lang?.startsWith("ca") ? "ca" : "es",
    gangOutput: "",
    gangNameOutput: "",
    kidNameOutput: "",
    attributesOutput: "",
    backgroundOutput: "",
    skillOutput: "",
    diceOutput: "",
    diceNotation: "1d3",
    lastGangText: "",

    init() { this.gangOutput = escapeHtml(this.t("ready")); },
    get data() { return TOOLS_DATA[this.lang] ?? TOOLS_DATA.es; },
    t(key) { return TOOLS_I18N[this.lang]?.[key] ?? TOOLS_I18N.es[key] ?? key; },

    generateGangName() {
      const result = this.randomGangName();
      this.gangNameOutput = renderRollLine(result.name, result.rolls);
    },
    generateKidName() {
      const result = this.randomKidName();
      this.kidNameOutput = renderRollLine(result.name, result.rolls);
    },
    generateAttributes() {
      const result = randomAttributeSet();
      this.attributesOutput = `<strong>1D6: ${result.roll}</strong><br>${result.values.join(", ")}`;
    },
    generateBackground() { this.backgroundOutput = renderNumberedEntry(pickNumbered(this.data.backgrounds), this.t("empty")); },
    generateSkill() { this.skillOutput = renderNumberedEntry(pickNumbered(this.data.skills), this.t("empty")); },
    rollDice() {
      const result = rollNotation(this.diceNotation);
      this.diceOutput = `<strong>${this.diceNotation.toUpperCase()}: ${result.total}</strong><br>${result.rolls.join(" + ")}`;
    },

    randomGangName() {
      const first = pickD100(this.data.clubA);
      const second = pickD100(this.data.clubB);
      return { name: `${first.value} ${second.value}`, rolls: `${this.t("tableA")} ${first.roll}, ${this.t("tableB")} ${second.roll}` };
    },
    randomKidName() {
      const first = pickD100(this.data.firstNames);
      const second = pickD100(this.data.surnames);
      return { name: `${first.value} ${second.value}`, rolls: `${this.t("firstName")} ${first.roll}, ${this.t("surname")} ${second.roll}` };
    },
    generateGang() {
      const gangName = this.randomGangName();
      const usedBackgrounds = new Set();
      const kids = [];
      for (let index = 0; index < 4; index += 1) {
        kids.push({
          kidName: this.randomKidName(),
          attributes: assignAttributes(randomAttributeSet().values),
          background: pickUniqueNumbered(this.data.backgrounds, usedBackgrounds)
        });
      }
      this.gangOutput = [
        `<h3>${escapeHtml(gangName.name)}</h3>`,
        `<p><strong>${this.t("rolls")}:</strong> ${escapeHtml(gangName.rolls)} · <strong>${this.t("initialBottles")}:</strong> 50 · <strong>${this.t("initialMood")}:</strong> 8</p>`,
        `<div class="kid-list">`,
        ...kids.map((kid, index) => this.renderKidCard(kid, index)),
        `</div>`
      ].join("");
      this.lastGangText = this.textGang(gangName, kids);
    },
    renderKidCard(kid, index) {
      const a = kid.attributes;
      return `
        <article class="kid-card">
          <h4>${index + 1}. ${escapeHtml(kid.kidName.name)}</h4>
          <p><strong>${this.t("rolls")}:</strong> ${escapeHtml(kid.kidName.rolls)} · <strong>${this.t("background")} ${kid.background.roll}:</strong> ${escapeHtml(kid.background.title)}</p>
          <dl>
            <div><dt>${this.t("mind")}</dt><dd>${formatSigned(a.mind)}</dd></div>
            <div><dt>${this.t("heart")}</dt><dd>${formatSigned(a.heart)}</dd></div>
            <div><dt>${this.t("body")}</dt><dd>${formatSigned(a.body)}</dd></div>
            <div><dt>${this.t("cool")}</dt><dd>${formatSigned(a.cool)}</dd></div>
            <div><dt>${this.t("health")}</dt><dd>${a.health}</dd></div>
            <div><dt>${this.t("movement")}</dt><dd>${a.movement} ${this.t("cells")}</dd></div>
          </dl>
          <p>${escapeHtml(kid.background.body)}</p>
        </article>`;
    },
    textGang(gangName, kids) {
      const lines = [gangName.name, `${this.t("rolls")}: ${gangName.rolls}`, `${this.t("initialBottles")}: 50`, `${this.t("initialMood")}: 8`, ""];
      kids.forEach((kid, index) => {
        const a = kid.attributes;
        lines.push(`${index + 1}. ${kid.kidName.name}`);
        lines.push(`   ${kid.kidName.rolls}`);
        lines.push(`   ${this.t("mind")} ${formatSigned(a.mind)}, ${this.t("heart")} ${formatSigned(a.heart)}, ${this.t("body")} ${formatSigned(a.body)}, ${this.t("cool")} ${formatSigned(a.cool)}`);
        lines.push(`   ${this.t("health")} ${a.health}, ${this.t("movement")} ${a.movement} ${this.t("cells")}`);
        lines.push(`   ${this.t("background")} ${kid.background.roll}: ${kid.background.title}`);
        lines.push("");
      });
      return lines.join("\n");
    },
    async copyGang() {
      if (!this.lastGangText) this.generateGang();
      try {
        await navigator.clipboard.writeText(this.lastGangText);
        this.gangOutput = `<pre>${escapeHtml(this.lastGangText)}</pre><p>${escapeHtml(this.t("copied"))}</p>`;
      } catch {
        this.gangOutput = `<pre>${escapeHtml(this.lastGangText)}</pre><p>${escapeHtml(this.t("copyFailed"))}</p>`;
      }
    }
  }));
});

function pickD100(entries) {
  if (!entries?.length) throw new Error("Empty D100 table");
  const roll = die(100);
  return entries.find((entry) => entry.roll === roll) ?? entries[roll % entries.length];
}
function pickNumbered(entries) {
  if (!entries?.length) return { roll: "—", title: "—", body: "" };
  return entries[Math.floor(Math.random() * entries.length)];
}
function pickUniqueNumbered(entries, used) {
  let entry = pickNumbered(entries);
  let guard = 0;
  while (used.has(entry.roll) && guard < 200) { entry = pickNumbered(entries); guard += 1; }
  used.add(entry.roll);
  return entry;
}
function randomAttributeSet() {
  const roll = die(6);
  if (roll <= 2) return { roll, values: ["+2", "0", "0", "−2"] };
  if (roll <= 4) return { roll, values: ["0", "0", "0", "0"] };
  return { roll, values: ["+1", "0", "0", "−1"] };
}
function assignAttributes(values) {
  const shuffled = [...values].sort(() => Math.random() - 0.5);
  const mind = parseSigned(shuffled[0]);
  const heart = parseSigned(shuffled[1]);
  const body = parseSigned(shuffled[2]);
  const cool = parseSigned(shuffled[3]);
  return { mind, heart, body, cool, health: 5 + heart, movement: Math.ceil((5 + body) / 2) };
}
function rollNotation(notation) {
  const match = /^(\d+)d(\d+)$/i.exec(String(notation).trim());
  if (!match) throw new Error(`Invalid dice notation: ${notation}`);
  const count = Number.parseInt(match[1], 10);
  const sides = Number.parseInt(match[2], 10);
  const rolls = Array.from({ length: count }, () => die(sides));
  return { rolls, total: rolls.reduce((sum, value) => sum + value, 0) };
}
function die(sides) { return Math.floor(Math.random() * sides) + 1; }
function renderRollLine(name, rolls) { return `<strong>${escapeHtml(name)}</strong><br><span>${escapeHtml(rolls)}</span>`; }
function renderNumberedEntry(entry, emptyText = "") {
  if (!entry || entry.roll === "—") return escapeHtml(emptyText);
  return `<strong>${entry.roll}. ${escapeHtml(entry.title)}</strong><br>${escapeHtml(entry.body)}`;
}
function parseSigned(value) { return Number.parseInt(String(value).replace("−", "-").replace("+", ""), 10) || 0; }
function formatSigned(value) { if (value > 0) return `+${value}`; if (value < 0) return `−${Math.abs(value)}`; return "0"; }
function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}
